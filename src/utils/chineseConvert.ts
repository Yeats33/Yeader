const T2S_MAP: Record<string, string> = Object.fromEntries(
  Object.entries(S2T_MAP).map(([s, t]) => [t, s])
);

export type ChineseScript = "original" | "simplified" | "traditional";

export function convertChineseScript(text: string, target: ChineseScript): string {
  if (target === "original") return text;
  const map = target === "traditional" ? S2T_MAP : T2S_MAP;
  return text.replace(/[一-鿿]/g, (ch) => map[ch] ?? ch);
}
